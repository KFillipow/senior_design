package com.example.senior_design;

public class SortByXValue {
    int x_value;
    double y_value;

    public SortByXValue(int x_value, double y_value) {
        this.x_value = x_value;
        this.y_value = y_value;
    }
    public int getX_value() {
        return x_value;
    }

    public double getY_value() {
        return y_value;
    }


}
